package com.musicfly.backend.models;

/**
 * Este enum define los tipos de redes sociales disponibles en el sistema.
 * Estos valores se utilizan para categorizar y almacenar las distintas cuentas
 * de redes sociales de los artistas o usuarios dentro de la aplicación.
 * Los valores comunes como Instagram, Spotify, YouTube y SoundCloud están representados aquí.
 */
public enum SocialMediaTypes {
    INSTAGRAM,
    SPOTIFY,
    FACEBOOK,
    GITHUB,
    YOUTUBE,
    SOUNDCLOUD,
    CUENTA_UNEX
}
